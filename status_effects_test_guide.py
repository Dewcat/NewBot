print("   选择添加以下技能：")
print("   - 强壮 (ID: 17) - 增益技能，增加攻击伤害")
print("   - 守护 (ID: 18) - 增益技能，减少受到伤害")
print("   - 呼吸法 (ID: 23) - 增益技能，增加暴击率")
print("   - 护盾术 (ID: 24) - 增益技能，抵消伤害")
print()

print("3. 为敌方角色添加减益技能：")
print("   /sm 邪恶法师")
print("   选择添加以下技能：")
print("   - 烧伤攻击 (ID: 19) - 伤害+减益技能")
print("   - 中毒箭 (ID: 20) - 伤害+减益技能")
print("   - 虚弱诅咒 (ID: 21) - 减益技能")
print("   - 易伤标记 (ID: 22) - 减益技能")
print()

print("4. 加入战斗:")
print("   /join 艾丽丝 邪恶法师")
print()

print("5. 测试增益技能:")
print("   /attack")
print("   选择艾丽丝 → 选择强壮技能 → 选择艾丽丝作为目标")
print("   /show 艾丽丝  # 查看状态效果")
print()

print("6. 测试减益技能:")
print("   /enemy_attack")
print("   选择邪恶法师 → 选择烧伤攻击 → 选择艾丽丝作为目标")
print("   /show 艾丽丝  # 查看减益状态")
print()

print("7. 测试状态效果在伤害计算中的作用:")
print("   /attack")
print("   选择艾丽丝 → 选择普通攻击 → 选择邪恶法师")
print("   观察强壮状态如何增加伤害")
print()

print("8. 测试回合结束处理:")
print("   /end_turn")
print("   观察烧伤效果如何在回合结束时造成伤害")
print("   观察状态效果的层数减少")
print()

print("9. 测试护盾效果:")
print("   /attack")
print("   选择艾丽丝 → 选择护盾术 → 选择艾丽丝")
print("   /enemy_attack")
print("   选择邪恶法师 → 选择普通攻击 → 选择艾丽丝")
print("   观察护盾如何抵消伤害")
print()

print("10. 测试复杂状态组合:")
print("    先给艾丽丝施加多个增益和减益")
print("    然后测试它们如何相互作用影响战斗")
print()

print("=== 技能分类说明 ===")
print("伤害技能：直接造成伤害（普通攻击、火球等）")
print("治疗技能：恢复生命值（净化）")
print("增益技能：施加有利状态效果（强壮、守护等）")
print("减益技能：施加不利状态效果（虚弱诅咒、易伤标记）")
print()

print("=== 状态效果详细说明 ===")
print()
print("💪 增益效果:")
print("- 强壮：攻击技能最终伤害+(层数×10%)")
print("- 呼吸法：暴击率+强度×1%，暴击伤害120%")
print("- 守护：受到最终伤害-(层数×10%)")
print("- 护盾：受到最终伤害-护盾强度，护盾会被消耗")
print()

print("💀 减益效果:")
print("- 烧伤：每回合结束按强度×1点扣血")
print("- 中毒：每回合结束按强度×1%体力扣血")
print("- 破裂：受击时按强度×1点扣血并减1层")
print("- 流血：行动后按强度×1点扣血并减1层")
print("- 虚弱：攻击技能最终伤害-(层数×10%)")
print("- 易伤：受到最终伤害+(层数×10%)")
print()

print("=== 状态效果叠加规则 ===")
print("- 同名状态效果：强度取较高值，层数累加")
print("- 每回合结束，大部分状态效果层数-1")
print("- 破裂和流血：受击/行动时触发并减层数，回合结束不减层")
print("- 护盾：受到伤害时消耗，不受回合限制")
print()

